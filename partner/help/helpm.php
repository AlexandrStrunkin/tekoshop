        <p class="MsoNormal"><img width="623" height="343" src="/upload/medialibrary/4f5/m01.png" title="m01.png" alt="m01.png"  /><o:p></o:p></p>
       
        <p class="MsoNormal">Все поля являются обязательными для заполнения. <b>Вид деятельности</b> необходимо выбрать только реально-действующие компетенции, а не желаемые. В дальнейшем изменить Вид деятельности возможно будет только по запросу. В случае негативного отзыва клиента возможно понижение рейтинга вашей организации. <o:p></o:p></p>
       
        <p class="MsoNormal">После обязательной проверки нашим менеджером введенной информации, вам придет уведомление на электронный адрес об одобрении или отказе регистрации. Далее вам станет доступен новый функционал партнера в Личном кабинете.<o:p></o:p></p>
       
        <p class="MsoNormal"><o:p> </o:p></p>
       
        <p class="MsoListParagraph" style="">2.<span style="font-stretch: normal; font-size: 7pt;">       </span><b>Заполнение профиля</b><o:p></o:p></p>
       
        <p class="MsoNormal">Заполнение профиля необходимо для получения клиентами полной и достоверной информации о Вашей компании. Редактирование профиля доступно в Личном кабинете<span lang="EN-US"><o:p></o:p></span></p>
       
        <p class="MsoNormal"><img width="624" height="451" src="/upload/medialibrary/c55/m02.png" title="m02.png" alt="m02.png"  /><o:p></o:p></p>
       
        <p class="MsoNormal">Старайтесь заполнить максимальное количество полей, т.к. клиент при выборе партнера очень внимательно относится к представленной информации. Указывайте только действующие/рабочие телефоны и адреса.<o:p></o:p></p>
       
        <p class="MsoNormal"><img width="449" height="663" src="/upload/medialibrary/3e7/m03.png" title="m03.png" alt="m03.png"  /><o:p></o:p></p>
       
