        <p class="MsoNormal">Старайтесь описать как можно больше информации о предложении, для получения быстрого ответа от клиента. Выберите оборудование из каталога, на котором вы предлагаете монтаж. Предложенное оборудование может отличаться от заявки клиента, если вы считаете, что ваше предложение будет более экономичным или эффективным. Укажите стоимость оборудования, отсутствующего в списке. Прикрепите файл с коммерческим предложением или сметой.<o:p></o:p></p>
       
