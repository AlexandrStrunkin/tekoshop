        <p class="MsoNormal">Подгрузите ваш логотип и Доступные лицензии на осуществление деятельности (например, лицензию на монтаж пожарного оборудования)<o:p></o:p></p>
       
        <p class="MsoNormal">Нажмите <b>Сохранить</b> для применения изменений. В случае необходимости Вы можете позже вернуться в данный раздел и продолжить редактирование.<o:p></o:p></p>
       
