        <p class="MsoNormal">Изучите запрос клиента, и составьте ему предложение, воспользовавшись кнопкой <b>Составить предложение</b>. Предложение можно будет отредактировать, если ваши условия поменялись в дальнейшем.<o:p></o:p></p>
       
        <p class="MsoNormal"><o:p> </o:p></p>
       
