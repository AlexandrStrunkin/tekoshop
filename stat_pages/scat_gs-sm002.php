  <br />
 Такая схема работы системы позволяет не только максимально обезопасить объекты любой значимости от преступного посягательства, но и что самое важное даёт возможность задержать преступника на месте преступления. Кроме того, одним из важных преимуществ устройства является невозможность сдачи объекта под охрану при открытом положении замка. </p>
 
<p>Система &laquo;Скат&raquo; может также комплектоваться дополнительными блоками управления охранно-пожарной сигнализации различной степени сложности. </p>
 
<p>Мы вплотную работаем с ГУВД по Московской области по внедрению данных систем, второй год выходят приказы по ГУВД о проведении целевой профилактической операции &laquo;Безопасная квартира, подъезд, дом&raquo;. </p>
 
