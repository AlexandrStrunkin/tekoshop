<p>По данным МВД, до последнего времени происходило до 500 000 квартирных краж в год. За последнее время их количество снизилось до 300 000 краж в год. Но в настоящее время, в связи со сложившейся кризисной ситуацией в стране (по заявлению заместителя председателя Государственной Думы по безопасности) ожидается рост квартирных краж. А так как квартирные кражи составляют 40% от общего числа правонарушений, а их раскрываемость составляет всего около 4%, то велика вероятность возврата криминальной обстановки предыдущих лет. Под охраной МВД находится примерно 3.5 млн. объектов, а под охраной ЧОП находится примерно 2.5 млн. объектов. Итого около 6 млн. объектов по стране. Среди всех краж примерно 1% составляют спланированные (профессиональные). Соответственно основная масса происходящих краж осуществляется через дверь. Двери открываются посредством вскрытия замка. Самые распространённые виды вскрытия замков &ndash; механический взлом замка с помощью &laquo;свертыша&raquo; или специального инструмента, а также вскрытие замков &laquo;путем подбора ключа&raquo; (отмычками). </p>
 
<p>Большинство замков, установленных в металлических дверях, может быть открыто методом механического разрушения в течение нескольких минут. Практически квартиры и охраняемые объекты беззащитны перед преступниками. </p>
 
<p>В последнее время по неофициальным данным участились случаи проникновения злоумышленников на охраняемые объекты. </p>
 
<p>Большую проблему в настоящее время составляют вопросы запасных и противопожарных дверей, с финансовой точки зрения практически невозможно совместить легкую свободу выхода из охраняемых помещений и надежную сохранность материальных ценностей. </p>
 
<p>Следует также отметить слабую защищенность охраняемых объектов перед ударами террористов, а именно, незащищенность металлических дверей. Входные металлические двери в подъездах не снабжены замками с полицейскими функциями. Не защищены технические помещения жилых домов, а также чердаки, подвалы. </p>
 
<p>Предприятие ООО &laquo;Класс-К&raquo; (г.Железнодорожный, М.О.) разработало и внедрило в производство новою инновационную сигнально &ndash; запирающую систему &laquo;Скат&raquo;, предназначенную для защиты от несанкционированного проникновения посторонних лиц как на охраняемый объект, так и на любые другие помещения. </p>
 
<p>Необходимость разработки Системы связано с неутешительной статистикой краж. Несмотря на усилия организаций занимающихся разработкой технических требований, норм и методов испытаний на замки с учетом преступных воздействий, количество преступлений в этой области снижается значительно медленнее, чем ожидалось. Одной из причин является малая раскрываемость преступлений, связанная с невозможностью поймать преступников на месте преступления, даже в том случае, когда квартира, либо другой объект находятся под охраной в отделениях вневедомственной охраны МВД РФ. 
  <br />
   
  <br />
 Поскольку сигнал поступает на пульт охраны тогда, когда преступник уже находится в помещении, далее следует ожидания звонка от хозяина помещения в течении 5 &ndash; 10 минут и тогда после того как звонок не поступил, выезжает милицейский наряд, который по нормативу должен прибыть через 10 минут. Итого у преступника имеется 15 &ndash; 20 минут времени, что более чем достаточно для кражи и ухода с места преступления. </p>
 
<p>Разработанная фирмой &laquo;Класс-К&raquo; сигнально &ndash; запирающая система &laquo;Скат&raquo; должна кардинально изменить ситуацию. Основой системы является серия замков сувальдных усиленной конструкции, соответствующих 4 классу стойкости к криминальному воздействию по ГОСТ 5089 &ndash; 2003, снабженных специальными защитными элементами от силового воздействия и вскрытия отмычками. 
  <br />
