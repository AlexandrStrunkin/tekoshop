  <br />
 Время, требуемое, для криминального вскрытия замков данной серии составляет от 30 &ndash; 60 мин., силовое воздействие на элементы замков 2000 кг., что многократно превышает нормативные требования по ГОСТ 5089 &ndash; 2003 и подтверждено результатами испытаний в лаборатории ФСБ, НИЦ &laquo;Охрана&raquo;, ИЦ &laquo;Замок&raquo; и сертификатом соответствия. Продукция ООО &laquo;Класс-К&raquo; последнего поколения по своим техническим характеристикам превышают аналоговую продукцию Российских и зарубежных производителей. Продукция запатентована. Второй элемент системы &ndash; это магнитно &ndash; контактный извещатель, установленный.. . непосредственно в замке. </p>
 
<p>При попытке вскрытия замка любым несанкционированным способом выбивание, высверливания или при помощи отмычек, срабатывает извещатель и в зависимости от заданной программы сигнал &laquo;Тревога&raquo; поступает на пульт охраны на начальном этапе вскрытия замка, либо включает другие функции &ndash; световая или звуковая сигнализация, подача сигнала дежурному на входе в здание, консьержку и т.д. 
  <br />
