<p style="font-weight: bold;"> Из чего же сделаны&hellip; </p>
 
<p> Базовая и неделимая часть прибора &laquo;Карат&raquo; &mdash; это центральный блок (ЦБ) + блок индикации и управления (БИУ). Они хоть и выполнены в виде отдельных устройств, но работать друг без друга не могут (рис. 2,3). Как правило, оба этих блока устанавливаются рядом, но при необходимости БИУ можно удалить от ЦБ на расстояние до 1000 м. </p>
 
<p> К ЦБ можно подключить до 24 шлейфов охранно-пожарной сигнализации (ШС). При использовании блоков расширения БШС-4, число шлейфов увеличивается до 250. </p>
 
