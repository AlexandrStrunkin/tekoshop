<h1>Карат &mdash; профессиональное решение для комплексной системы безопасности</h1>
 
<p> Представьте, что Вы руководитель проектно-монтажной организации в сфере охранно-пожарной безопасности, и к Вам обратился серьезный клиент. Он просит разработать и установить в его многоэтажном центральном офисе комплексную систему безопасности. Помимо охранно-пожарной сигнализации, система должна выполнять функции контроля доступа, иметь оповещение о пожаре, управлять в автоматическом режиме дымоудалением, освещением, контролировать возможные протечки воды и т.д. Плюс, нужно предусмотреть расширение этой системы в будущем. </p>
 
<p style="font-weight: bold;"> Какие Ваши действия? </p>
 
<p> Можно с ходу предложить несколько импортных систем, которые стоят как полсамолета и которые придется дорабатывать с учетом российских требований охранно-пожарной безопасности. Можно из приборов разных производителей собрать &laquo;солянку&raquo;, и получить возможность еще раз подтвердить пословицу, что скупой платит дважды. А можно выбрать золотую середину, т.е. использовать российского оборудование от одного производителя. </p>
 
<p style="font-weight: bold;"> Золотая середина </p>
 
<p> Крупнейший российский производитель систем безопасности НПО &laquo;Сибирский Арсенал&raquo; предлагает для таких задач использовать прибор &laquo;Карат&raquo;. Он способен реализовать все, что расписано в первом абзаце, а кроме этого, позволяет делать такие вещи, которые удивляют даже создателей этого прибора. </p>
 
