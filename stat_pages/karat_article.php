<p> Система водоснабжения есть в каждом многоквартирном доме. Она несет с собой много удобств, пока находится в исправном состоянии. Но если вовремя не заметить протечку&hellip; Те, кого заливали соседи сверху, поймут о чем речь. Так вот, &laquo;Карат&raquo; может контролировать утечки холодной и горячей воды. При ее обнаружении он самостоятельно перекроет кран и предотвратит потоп. </p>
 
